void TAboutDlg::setState( ushort aState, Boolean enable )
{
	if( aState & sfVisible )
		{
		if( enable )
			{
			TSpeedWindow *sw = new TSpeedWindow( getBounds(), True, 0 );
			TDialog::setState( aState, enable );
			destroy( sw );
			return;
			}
		else
			{
			TProgram::deskTop->lock();
			TDialog::setState( aState, enable );
			TSpeedWindow *sw = new TSpeedWindow( getBounds(), False, 0 );
			destroy( sw );
			return;
			}
		}

	TDialog::setState( aState, enable );
}


