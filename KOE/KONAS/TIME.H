#ifndef TIME
#define TIME


#include "Types.H"


// Die "Time"-Klasse speichert eine Uhrzeit.
// Beim Setzen einer neuen Zeit wird diese auf Korrektheit berprft.


class Time
{
public:
	//=== constructors and destructor =====
	Time();
        // Nach der Ausfhrung des Contructors enth„lt dieses Objekt die
        // aktuelle System-Zeit.

	void setToCurrentTime();
        // Setzt die Daten dieses Objekt auf die aktuelle System-Zeit.

	boolean set( char aHour, char aMinute, char aSecond );
        // Setzt die Daten dieses Objekts und liefert True zurck. Falls die
        // bergebenen Parameter nicht korrekt sind, wird False
        // zurckgeliefert und die vorher gespeicherten Werte bleiben
        // erhalten.
        // (Korrekte Bereiche fr die Parameter: "aHour" 0 - 23,
        // "aMinute" und "aSecond" 0 - 59)

	char getHour();
	char getMinute();
	char getSecond();
        // Diese Methoden liefern die Stunde, Minute und Sekunde dieses
        // Objekts.

protected:
	char hour;
	char minute;
	char second;
};


inline char Time::getHour()
	{ return hour; }

inline char Time::getMinute()
	{ return minute; }

inline char Time::getSecond()
	{ return second; }


#endif